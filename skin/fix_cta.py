# デフォルトのハッシュタグ（note URLありでタグなし投稿に付ける）
DEFAULT_TAGS = "#肌荒れ #大人ニキビ #肌改善 #スキンケア"


def extract_hashtags(text):
    """テキスト末尾のハッシュタグ行を抽出して返す"""
    tags = re.findall(r'#\S+', text)
    if tags:
        return " ".join(tags)
    return None


def extract_note_url(text):
    """テキスト内のnote URLを含む行を抽出して返す（例: '→ https://note.com/...'）"""
    match = re.search(r'→ https://note\.com/\S+', text)
    if match:
        return match.group(0)
    return None


def build_new_ending(theme, original_text):
    """テーマに合った新しい3本目末尾テキストを組み立てる"""
    question = THEME_QUESTIONS.get(theme)
    if not question:
        return None

    hashtags = extract_hashtags(original_text)
    note_url = extract_note_url(original_text)

    parts = []
    if note_url:
        # note URLがある投稿：URL行 → 空行 → 質問 → 空行 → タグ
        parts.append(note_url)
        parts.append("")
        parts.append(question)
        parts.append("")
        parts.append(hashtags if hashtags else DEFAULT_TAGS)
    else:
        # note URLなし：質問 → 空行 → タグ
        parts.append(question)
        parts.append("")
        parts.append(hashtags if hashtags else DEFAULT_TAGS)

    return "\n".join(parts)


def transform(data, dry_run=True):
    """フォロー系CTAをコメント促進型に変換する"""
    changed = 0
    skipped_no_mapping = []

    for post in data["threads_a"]:
        last_text = post["posts"][-1] if post["posts"] else ""

        if "フォロー" not in last_text:
            continue

        new_ending = build_new_ending(post["theme"], last_text)

        if new_ending is None:
            skipped_no_mapping.append((post["id"], post["theme"]))
            continue

        if dry_run:
            print(f"\n--- id:{post['id']} [{post['theme']}] ---")
            print("【変換前】")
            print(last_text[:200])
            print("【変換後】")
            print(new_ending)
        else:
            post["posts"][-1] = new_ending

        changed += 1

    return changed, skipped_no_mapping


