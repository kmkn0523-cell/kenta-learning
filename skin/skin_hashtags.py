# skin_hashtags.py
# skinアカウント用：ハッシュタグを4セットで回す（発見タブ流入を広げる）
# 投稿カウントを渡すと A→B→C→D→A... の順でセットを返す純粋関数
# （固定5個の連投はスパム判定リスクがあるため、3個×4セットのローテに変更）

# 4つのハッシュタグセット（1セット3個に抑える＝スパム判定回避）
HASHTAG_SETS = {
    "A": "#肌荒れ #大人ニキビ #スキンケア",      # 王道（悩み直撃の基本セット）
    "B": "#腸活 #美肌習慣 #インナーケア",        # 内側ケア（腸活・習慣層へ）
    "C": "#ニキビ #毛穴ケア #敏感肌",            # 症状別（具体的な悩み検索へ）
    "D": "#美肌 #肌活 #スキンケア初心者",        # 行動（これから始める層へ）
}

# 回す順番（リストの順にローテーションする）
_ROTATION = ["A", "B", "C", "D"]


def pick_hashtag_set(count):
    """投稿カウント(0始まり)から、その回に使うハッシュタグ文字列を返す"""
    key = _ROTATION[count % len(_ROTATION)]  # 余りでA〜Dを選ぶ
    return HASHTAG_SETS[key]
